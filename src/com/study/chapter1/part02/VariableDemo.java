package com.study.chapter1.part02;

/**
 * 变量使用示例
 */
public class VariableDemo {
    public static void main(String[] args) {
        // 1. 变量的声明
        int a;
        int b, c;

        // 2. 变量的初始化
        a = 10;
        b = 20;
        int d = 30;
        int e = 40, f = 50;

        // 3. 变量的使用
        // System.out.println(c);
        System.out.println(a);
        System.out.println(b);

        // 4. 变量的命名
        int score;
        int javaScore;
        int myJavaScore;
    }

}
